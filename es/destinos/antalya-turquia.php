<h2>Viaje a Antalya</h2>
<p>Está ubicada al suroeste de Turquía en el Golfo de Antalya en la costa mediterránea, se caracteriza por la belleza de sus tierras, pintorescas calles y playas, con una arquitectura única que muestra El Paso de los siglos, Antalya fue parte del Imperio Hitita,  y después de este variados personajes tomaron el golfo como ruta invasora, desde los persas hasta el cristianismo con la llegada de Pablo de Tarso. Pero además de ello, parece que Antalya, específicamente en la zona de las Cuevas de Karain, fue cuna de los primeros rastros de vida humana, hace unos 100.000 años A.C. A principios del Siglo XIII,  fueron los turcos selyúcidas quienes conquistaron esta región y  hacia el siglo XX durante la I Guerra Mundial, la región fue ocupada por italianos, hasta 1923, que fue el año de la creación de la república de Turquía.</p>
<p>Desde finales del siglo XX, se ha convertido en potencia turística en Turquía recibiendo millones de turistas anuales, su población es mayoritariamente turca, seguidos de griegos y un bajo porcentaje de otras nacionalidades entre ellos rusos, alemanes, e ingleses.</p>
<h3>Puerta de Adriano</h3>
<p>Puerta o arco de Adriano se construyó en nombre del emperador romano Adriano y durante muchos años literal fue la puerta de entrada y salida, es de mármol y estéticamente hablando está muy bien conservada y por demás bien ubicada por ello el lugar es emblemático y frecuentemente visitado.</p>
<h3>Torre del Reloj Antalya</h3>
<p>Según datos históricos la torre fue construida en el siglo II después de Cristo, y es la sobreviviente de 80 torres que formaban un muro. como es bien sabido en la antigüedad la población carecía de relojes y para divulgar la hora de una manera práctica se construían una torre con grandes relojes para que los habitantes pudieran ver la hora con facilidad, un ejemplo de esto es esta torre junto al parque Karaalioglu y Kaleici, hoy centro histórico.</p>
<h3>Teatro de Aspendos</h3>
<p>Está ubicada a 40 Kilómetros de Antalya, es ciudad con la herencia de la cultura grecorromana más imponente debido a que tiene el teatro romano mejor conservado de todo el mundo, entonces podrás imaginar  la acústica e infraestructura que tenía una capacidad de 15.000 personas.</p>
<h3>Cueva de Karain</h3>
<p>Es un sitio arqueológico que data del paleolítico, esta cueva de la prehistoria está a unos 28 km de la ciudad de Antalya en la localidad de Yagca. También se le conoce como cueva negra.</p>
