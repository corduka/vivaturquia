<h2>Ankara</h2>
<p>La actual capital de Turquía, está ubicada en la región de Anatolia en el centro del país, tiene aproximadamente 6 mil millones de habitantes hecho que la convierte en la segunda ciudad más habitada de todo el país, como parte de la historia de la región de Anatolia, parece ser que la civilización de los Hititas prosperaron allí durante la edad de bronce, pero, mas adelante la ciudad fue conquistada por el imperio romano y pasó a llamarse Ancyra y en los siglos venideros, la zona tomó importancia militar y logística para el imperio bizantino, ya en el periodo otomano se abren las puertas a los turcos Selyucidas y el 29 de octubre de 1923, en el momento de la fundaciòn de la República de Turquía Ankara pasaría a ser La Capital sustituyendo a Estambul la antigua Constantinopla.</p>
<p>La ciudad en la actualidad presenta aires de modernidad con grandes hoteles, teatros, centros comerciales y rascacielos. De hecho la mayoría de oficinas gubernamentales y embajadas extranjeras se encuentran allí. </p>
