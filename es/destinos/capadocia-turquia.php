<h2>Capadocia Chimeneas de las Hadas, Atracciones Turísticas y Globo Aerostático</h2>
<p>Gracias a la erosión de depósitos volcánicos neógenos y cuaternarios o  capas blandas de lava y cenizas de provenientes de los montes Erciyes, Hasan y Gullu, hace aproximadamente 60 millones de años se formó la impresionante región de Capadocia, y lo que hoy conocemos como “Chimeneas de las Hadas” se formó hace 11 o 12 millones de años y no culminaría sino hasta hace seis millones de años atrás, en Goreme por ejemplo, el agua y el viento esculpieron una topografía singular que permitieron que grupos humanos aprovecharán esta geografía como refugio y defensa, hay una arquitectura rupestre que con el paso de los años se ha ido modernizando, por eso podrás ver hoteles caves hotel de lujo color a piedra y sensación de antigüedad. </p>
<p>La región permitió el paso de los cristianos, sus asentamientos y catedrales religiosas las cuales contienen expresiones pictóricas sacras y escenas narrativas de la Biblia, el Museo al aire libre es un testimonio vivo, así como también hay evidencias del paso de los musulmanes, signos de la convivencia entre estos dos grupos religiosos parte del siglo XVIII y principios del siglo XIX. </p>
<p>La región pasó a ser uno de los sitios turísticos más llamativos, por sus paseos en globo aerostático, sus paisajes, comidas y actividades varias que ofrecen únicos viajes Capadocia, por ejemplo en el último año entre enero y abril la región acogió alrededor de 8-8 millones de turistas.</p>
<p>El vasto paisaje abarca las provincias de Nevsehir, Kayseri, Aksaray, Kirsehir, Malatya, Sivas y Nigde. </p>
<h3><a href="<?= $base_url; ?>es/tours/tour-en-globo-aerostatico.php">Globo Capadócia Turquia</a>; Planes Imperdibles</h3>
<p>Los seres humanos siempre hemos sentido una gran curiosidad por volar, una de las formas que nos ha llevado a esta experiencia de una manera afable es el paseo en globo aerostático, técnicamente se calienta el aire al interior del globo lo que genera flotabilidad por la expansión del aire caliente. La primera demostración fue en el siglo XVIII, y ahora podrás conocer los valles más hermosos de Turquía al amanecer.</p>
<h3>Visita al Museo del Aire</h3>
<p>El museo al aire libre Goreme es una de las atracciones turísticas más llamativas; consiste en recintos cristianos, iglesias que hablan de tiempos de persecución y lucha, escenas bíblicas y ciudades subterráneas, aunque no todos los ocho niveles están abiertos al público podrás hacerte una idea de los matices de esa época que datan del siglo III aproximadamente, se encuentra a 1,5 Km del pueblo Göreme.</p>
<h3>Valle del Amor Turquia</h3>
<p>Al norte de la región de Capadocia se encuentran tres peculiares erupciones volcánicas con formaciones de falos que le dan al lugar el nombre del valle del amor, es un punto estratégico para conocer ya sea el Museo al aire libre o Avanos.</p>
<h3>Castillo de Uchisar</h3>
<p>La formación rocosa particular y hermosa se encuentra en lo más alto de la ciudad de Uchisar y se le dio forma de castillo, es un excelente punto para ver atardeceres y amaneceres, pues ofrece una increíble vista panorámica hacia el horizonte.</p>
<h3>Noche Turca</h3>
<p>Hay algo exquisito en la diferencia y en la diversión, cada sociedad a lo largo de la historia, ha desarrollado diferentes rituales en torno a la danza, el alcohol y la música, la noche turca es una experiencia tan fascinante como diferente.</p>
<p>Verás la danza de vientre o danza oriental, -una de las danzas más antiguas del mundo- increíbles colores y atuendos propios del folclor, otros tipos de danza folclórica y hasta muestras de bailes tradicionalmente usados en las bodas turcas. Capadocia ciudad; es el escenario perfecto para introducirte física y emocionalmente en la cultura turca.</p>
