<h3>Travertino de Pamukkale</h3>
<p>La montaña de Pamukkale o castillo de algodón cuenta con numerosas fuentes termales producto de movimientos tectónicos y la actividad volcánica que a lo largo de los años fueron ocasionando la formación de aguas termales justo en la depresión de la cuenca del río Menderes, entonces, gracias a que las aguas son elevadas en minerales como el creta, bicarbonato y calcio se les atribuye un carácter sanador, por otro lado este proceso de minerales expuesto al aire permite la formación de capas gruesas y blancas de piedras calizas, el curso del agua les da forma de cascada y por ello da la sensación de estar viento una cascada congelada.</p>
<p>El lugar se  ve a Km de distancia por su extrema blancura, pero a medida que te acercas te vas dando cuenta que sus aguas son cálidas.</p>
