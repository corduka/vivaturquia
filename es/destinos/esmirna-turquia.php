<h3>el templo de artemisa 7 maravillas del mundo antiguo</h3>
<p>Templo de Artemisa, también llamado Artemisio, templo en Éfeso es considerado una de las Siete Maravillas del Mundo antiguo.</p>
<p>El culto y reverencia hacia la diosa Artemisa llamada Diana por los romanos, era parte importante en esta región del mundo y en una época marcada por las guerras y el poder, ¿por qué es importante? ¿Quién es Artemisa? Ella era la diosa de la caza, los animales y la naturaleza quién protegía el territorio y a los más débiles.</p>
<p>En el siglo VII a.c., la ciudad fue atacada por los Cimerios quienes destruyeron gran parte del templo tras un gran incendio, no obstante, años después se ordenó levantar un nuevo templo ¿saben por qué? Porque la noche en la que fue destruido el templo nació Alejandro Magno 356 a.C., y este, al enterarse de este acontecimiento ordenó la construcción de uno nuevo.</p>
<h3>Biblioteca de Celso</h3>
<p>La biblioteca de Éfeso, es un importante monumento para el mundo, emblemático por su extraordinaria conservación después de su restauración en el siglo XX, el lugar fue erigido hacia el año 135 d.C., en principio fue una tumba donde se enterraría al procónsul de Asia junto con valiosos pergaminos,</p>
<p>En tu visita podrás admirar las estatuas en la entrada que protegen la sabiduría y representan verdaderas virtudes que poseía Celso: Reflexión, Sabiduría, Valor y Conocimiento (Ennoia, Sofía, Areté y Epistémé). La imponente fachada nos hace reconocer el invaluable lugar que Éfeso Biblioteca tuvo y sigue teniendo para la humanidad.</p>
<h3>Basílica de San Juan</h3>
<p>Fue construida para honrar el nombre del apóstol San Juan sobre el siglo VI, la basílica de San Juan Turquía ha tenido una gran importancia en el mundo romano, razón por la cual el lugar ha sido sitio de peregrinaciones y visitas del papado.</p>
<h3>efeso casa de la virgen maría</h3>
