<p>A orillas del Egeo se erigió una gran ciudad con influencia griega y Persia, a cuyo resultado se denomina el periodo Helenístico. Por aquella época 323 a.c hasta el año 30 a.c este lugar estuvo marcado por grandes evoluciones de carácter artístico, urbanístico y cultural.</p>
<p>Éfeso en Turquía tiene muchas de las huellas de este periodo rico en intelecto, por eso te queremos dar detalles de algunos de los lugares que marcaron este periodo para que visitar Turquía no sea solo un tour, sino una experiencia histórica!</p>
<ol>
    <h2><li>Gran Teatro de Éfeso</li></h2>
    <p>Con una altura de 18 metros, 3 pisos, 3 divisionesm 66 filas de asientos y 2 diazomas el Teatro de Éfeso es considera uno de los teatros más grandes de Turquía. Era parte del entretenimiento en la antigüedad, donde el teatro, la poesía y las artes plásticas florecieron, pero recuerden algo y es que en este periodo, aparecieron no solo centros culturales sino políticos, así 	une este teatro tuvo diversos fines, entre estos la acogida política.</p>
<h2><li>Templo de Artemisa</li></h2>
<q cite="https://www.bible.com/es/bible/149/ACT.19.21-40.RVR1960">¡Grande es Diana de los efesios!</q>
<p>El imponente templo de Artemisa, fue una de las más grandes y bellas obras arquitectónicas del mundo antiguo. Artemisa era la diosa de la naturaleza a la que los griegos adoraban con gran fervor, para el mundo cristiano esta es conocida como Diana ya que el lugar es mencionado en la Biblia, específicamente en Hechos 19:23-41.</p>
<p>El templo original fue quemado y Alejandro Magno posteriormente ordenó su reconstrucción. En la actualidad solo queda en pie una de sus columnas de mármol.</p>
<h2><li>Puerta de Adriano</li></h2>
<p>Como ya lo mencionamos, por la época Helenística las artes tuvieron un importante desarrollo, puedes verlo en las asculturas de Éfeso y en la que es considerada la puerta más hermosa de la ciudad de Éfeso, cada detalle del mármol tiene un valeroso e importante significado, se trata de un friso con escenas relacionadas con la fundación de la ciudad: Un jinete cazando un jabalí, Hércules rescatando a Teseo, héroes mitológicos, Hades, Dionisio y su séquito y también está tallado lo que parece ser una congregación de dioses entre la cual figura Atenea y Artemisa.</p>
<h2><li>Avenida de los curetes</li></h2>
<p>Hipodamo de Mileto, es el autor de dicha avenida, el reconocido padre del urbanismo, realizó la famosa calle que conecta la ciudad. La calle en sus inicios estaba adornada por fuentes, monumentos, columnas y lo mas importante, estatuas de las figuras políticas y religiosas más importantes, un dato curioso es que los cuerpos de muchas de esas estatuas están sin cabezas. Hay dos motivos detrás de este hecho; el primero es que la cabeza de las estatuas se cambiaba en algunos casos por si había algún cambio de “personaje” y la segunda razón es que debido a los diversos terremotos que ha habido en la zona se fueron cayendo con el paso de los siglos.</p>
<h2><li>Biblioteca de Celso</li></h2>
<q cite="https://www.researchgate.net/publication/356624593_TALES_DE_MILETO_UN_HEROE_DE_SABIDURIA_ENIGMATICA/download">El hombre solo alcanza la categoría de héroes mediante la sabiduría.</q> <span>CELSIUS</span>
<p>La reconocida biblioteca, se erigió sobre el año II d.c, su arquitectura de columnas (unas mas largas que otras) tiene un interesante recurso teatral que da una sensación de movimiento al verla, detalle artístico y arquitectónico bastante producido que da cuenta del gran intelecto de la época, además de los mas de 12.000 rollos de pergamino que reposaban en el lugar.</p>
<p>Fue construida en honor y sobre la tumba del senador y magistrado de Asia Tiberio Julios Celsius Polemeano, haciendo alusión a su gran sabiduría motivo por el cual las estatuas que se erigen en su entrada son, Sophia (sabiduría) Ennoia (Inteligencia) Episteme (Conocimiento) Arete (Virtud).</p>
