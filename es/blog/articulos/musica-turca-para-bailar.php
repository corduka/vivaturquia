<p>Hablar de Turquía su danza y música es hablar de la combinación de elementos culturales de la región Anatolia, la herencia otomana y la influencia de Oriente Medio, herencias étnicas de los griegos, armenios, albanos y la lista sigue.</p>
<p>Los  instrumentos y ritmos han llegado a lo largo de los años en su movida y larga historia.</p>
<p>Se puede hacer una clasificación de la música turca desde lo clásico de corte otomano, música de harén otomano, de corte militar, religiosa, hasta la música con influencia occidental, así pues, aquí hablaremos de los ritmos más tradicionales y eso implica hablar de la música turca tradicional, danzas tradicionales con influencia oriental como por ejemplo la famosa danza del vientre.</p>
<h2>Halay</h2>
<p>Música instrumental turca que hace parte del folclore en Turquía por lo que ha adoptado una danza propia. Es común en el centro, sur, este y sureste del país.</p>
<h2>Kasap Havası</h2>
<p>Sus orígenes se remontan a la época bizantina, está danza fue adoptada por el gremio de carniceros de Constantinopla y su popularidad se mantuvo hasta el siglo XX en los barrios hoy conocidos como Balta y Fener.</p>
<h2>Reyhani</h2>
<p>Una danza típica de Mardin conocida por ser un baile turco en las bodas, a modo de ritual, la danza y la  música se mezclan expresando gratitud a Dios y ofreciendo lealtad a la pareja.</p>
<h2>Derviches Giradores o Girovagos</h2>
<p>Danza muy famosa porque los danzantes giran sobre sí mismos con los brazos extendidos en un símbolo de espiritualidad del Sama que es una ceremonia mevlevi.</p>
<h2>Horon</h2>
<p>Tradicional en la zona norte de Turquía, sobre el Mar Negro, la gaita o Tulum y el Kemenche instrumento de cuerdas hacen de este baile uno de los más interesantes y pintorescos.</p>
<h2>Zeybek</h2>
<p>Danza folclórica de la región del Egeo, Zeybek fue un grupo de personas que alrededor del siglo XVI bailaban músicas resistiendo a los problemas sociales que habían en la era otomana.</p>
<p>La danza es un honor al coraje y al heroísmo, por lo que en la celebración de la creación de la República de Turquía, Ataturk decidió bailar junto con su pueblo esta danza en Esmirna.</p>
<h2>Kasik (Kaşık) Oyunu</h2>
<p>Danza acompañada de cucharas de madera en cada mano que genera un sonido parecido al de las castañuelas, es famoso en Konya.</p>
<h2>Oryantal o Danza del Vientre</h2>
<p>Tiene influencia de diferentes danzas de Oriente Próximo, algunos dicen que se originó el Egipto como un culto a la fertilidad, su interpretación la hace una sola bailarina con atuendos coloridos y accesorios dorados o color plata.</p>
