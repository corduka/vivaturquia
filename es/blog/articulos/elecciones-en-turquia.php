<h2>¿Qué pasó con Turquía después de las elecciones presidenciales de 2018?</h2>
<p>La elección en 2018 no fue solo para elegir al presidente. Con el nuevo sistema de Erdogan, el presidente pasó a tener una mayor poder. El parlamento ya se debilitó. Un hombre gobierna todo, un hombre elige y asigna jueces, fiscales, rectores universitarios, ministros y más. Un hombre controla el ejército. Él elige a los principales generales. Un hombre controla a la policía. Elige a los altos directivos del departamento de policía. Un hombre controla el servicio secreto que se llama MIT. Elige y cambia a los gerentes cuando quiere con un documento en un diario oficial, en menos de un día. Un hombre controla el banco central. Él decide cuándo las tasas de interés deben bajar o subir. Controla las reservas de divisas. Y por supuesto cambia al presidente del banco central cuando quiere. Por ejemplo, el presidente del banco central de Turkiye ha cambiado 4 veces en los últimos 4 años. Aquí está la prueba: <a href="https://www.tcmb.gov.tr/wps/wcm/connect/TR/TCMB+TR/Main+Menu/Banka+Hakkinda/Tarihce/Baskanlar" target="_blank">Banco Central de Turquía</a>.</p>
<p>Hablemos de economía: Ha habido hiperinflación en Turquía desde 2018 y está empeorando cada año. La tasa de inflación anual supera el 100% en este año. 1 USD era 4 TL en abril de 2021. Ahora 1 USD es 19,4 TL. 1 USD es 5 veces más caro que hace 5 años. Como muchos de los países del siglo XXI, Turquía también tiene una economía abierta. Es por eso que el USD tiene un efecto sobre los precios generales de los bienes y servicios. Entonces, ahora puede imaginar el cambio en la inflación en los últimos 5 años.</p>
