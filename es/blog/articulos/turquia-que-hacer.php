<p>Hay millones de actividades y cosas que hacer en Turquía, los planes son casi infinitos, playas, museos, restaurantes, parques… En fin, hay para todos los gustos, momentos y presupuestos. Pero, sabemos que las grandes experiencias también ser viven en los pequeños detalles, en aquello diferente y tradicional de cada cultura hay una riqueza simbólica y material que, consideramos debes experimentar.</p>
<p>Por eso, en este blog queremos recomendarte algunas actividades muy locales que no te puedes perder por nada del mundo.</p>
<h2>Tomar Té Turco en Lugares Locales</h2>
<p>Como ya debes saber el té o en turco “çay” es una bebida muy famosa en Asia, en Turquía particularmente toman té negro y  ser esencial para cualquier turco, su preparación es cuidadosa, motivo por el cual lo preparan en una tetera de dos pisos donde el vapor del agua hirviendo lo mantiene caliente.</p>
<p>Te recomendamos tomar té en lugares locales, para que sientas la atmósfera turca, muchas veces mientras toman té juegan sus tradicionales juegos de mesa “OK” o “Tavla”.</p>
<h2>Tomar Café Turco</h2>
<p>Su sabor es consistente, fuerte y delicioso; en ningún otro lugar del mundo el café lo preparan de una manera tan cuidadosa y única, para la preparación del café turco el café debe poseer una  textura en extremo fina, gracias a ello al momento de prepararlo se va generando una delicada espuma, la misma que al final del café genera un pequeño sedimento en el fondo de la copa, este hecho permite que una persona te lea el café ¿te animas a saber tu futuro?</p>
<p>Para acompañarlo te llevarán agua y dulces tradicionales turcos, la experiencia en sí, resulta ser todo un ritual, por esto el café turco o café a la turca, es patrimonio cultural inmaterial de la Humanidad.</p>
<h2>Visitar El Pazar Más Cercano</h2>
<p>Colores, frutas, olores, texturas y voces en un solo lugar: el mercado.</p>
<p>Este es uno de nuestros lugares favoritos, la economía local que sostiene muchas familias y nos facilita la vida a todos, vegetales frescos de sus jardines, frutas, huevos, aceitunas, especias, ropa, utensilios para el hogar todo en un mismo lugar: El Pazar que se abre un día por semana dependiendo de la ciudad, tiene todo lo más turco que puedes experimentar, desde su cálida gente, hasta frutas y vegetales que quizá nunca hayas visto, puedes ir y con 100 Liras comprar las frutas de la temporada.</p>
<h2>Jugar Tavla y Okey</h2>
<p>Si viste la famosa serie “Lost” recordarás que John Locke habla de este juego “Backgammon” dice que es el juego más viejo del mundo, conocido porque un grupo de arqueólogos lo descubrieron en Mesopotamia (o sea, Turquía)  tras unas excavaciones, se determinó entonces que el juego existe hace 5 mil años. En Turquía se llama Tavla, dos lados; dos colores: dos jugadores, tiras los dados y empieza el juego, debes trasladar todas las fichas de tu color a tu sección, cumpliendo una serie de reglas y castigos.</p>
<p>En cuanto a O.K lo puedes jugar con tus amigos, es un juego para cuatro personas, la finalidad del juego es confirmar grupos con patrones de números colores o secuencias numéricas. Es un juego basado en el dominó pero con  popularidad en Turquía.</p>
<h2>Narguile</h2>
<p>Aunque los orígenes del Narguile no son muy claras, su historia en Turquía se remonta al siglo XVII, al parecer llegaron por la región de Anatolia, se popularizó mucho desde entonces los sitios para fumar tabaco en Narguile, lugares tranquilos para reunirse con los amigos a conversar con café turco o té turco puede resultar una experiencia muy turca.</p>
<p>Nota aclaratoria: No pretendemos promover el uso del tabaco, la información aquí suministrada es sólo una muestra de actividades locales sobre Turquía.</p>
<h2>Tomar Boza (Bebida Tradicional)</h2>
<p>La boza es una de las bebidas turcas más tradicionales, fue consumida durante muchos años como una bebida embriagante antes de que llegaran las empresas de bebidas alcohólicas. Mustafa Kemal Atatürk el fundador de la República de Turquía fue un defensor de sus vendedores callejeros en Estambul, así como también fue un fiel consumidor. Existe un lugar en la ciudad de Estambul donde reposa en vaso en el que Atatürk bebió boza, su nombre es Vefa Bozacisi.</p>
