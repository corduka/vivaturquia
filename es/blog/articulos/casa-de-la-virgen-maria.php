<p>Al llegar, hay una reseña histórica del lugar, traducida a más de cinco idiomas, entre ellos el español, árabe, e italiano. La casa es pequeña y muchos afirman que muestra la humildad en la que vivió la Virgen María, por dentro no es posible realizar registros fílmicos o fotográficos, sin embargo, allí reposan algunos de los obsequios que ha dejado el papado en sus visitas.</p>
