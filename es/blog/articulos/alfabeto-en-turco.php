<h2>¿Y qué tiene qué ver Atatürk, el alfabeto y la modernidad?</h2>
<p>Antes de la adopción del alfabeto turco (con letras latinas), el idioma turco se escribía en caracteres árabes. Esta forma de escritura era inadecuada para el turco, ya que el árabe no tenía letras para algunos sonidos del turco, y tenía letras para sonidos que no existían en el turco.</p>
<p>Lo anteriormente señalado, se relacionaba profundamente con el Islám y la influencia de la religión en la sociedad turca, lo que para Atatürk -un hombre adelantado a su época- era un despropósito, sabiendo los índices de analfabetismo que había en la población, al realizar este importante cambio, empieza una era de alfabetización en campos y ciudades con el nuevo alfabeto.</p> 
<p>El alfabeto en turco consta de 29 letras, que incluyen 8 vocales y 21 consonantes. A diferencia del nuestro alfabeto en español, el turco no utiliza las letras “ñ”,"q", "w" y "x". En su lugar, se utilizan las letras "ğ", "ü" y "ş", “ö”, “ı” “ç”.</p>
<p>Otra ventaja del alfabeto turco es que es fácil de aprender y de usar. A diferencia de algunos otros sistemas de escritura, como el chino o el japonés, el alfabeto turco es completamente fonético, lo que significa que cada letra representa un sonido específico y cada sonido se representa por una letra específica. Esto hace que sea mucho más fácil de aprender y escribir en turco.</p>
<p>En conclusión, el alfabeto turco es un sistema de escritura fascinante y único que ha sido fundamental en la modernización de Turquía, así como la unificación de su lengua, facilitando la comunicación y la educación en el país.</p>
