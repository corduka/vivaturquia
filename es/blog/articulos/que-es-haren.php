                        <h2>¿De Dónde Eran Estas Mujeres?</h2>
<p>Es importante mencionar que el Islám es una religión que permite la poligamia, lo que les da “derecho” a los hombres de tener una o varias esposas. ¿Ahora pueden imaginar la figura de un sultán con poder político y religioso? Pues resulta que en los periodos de guerra, los sultanes solían regresar con mujeres de otros países, o en su defecto las compraban en los mercados de esclavas -que era parte de la economía-, al provenir de otros países u otras tierras, era mujeres no otomanas, es decir, no musulmanas, lo que las convertía en mujeres sin derechos, que debían ser educadas y salvadas del pecado de no ser otomanas o musulmanas.</p>
