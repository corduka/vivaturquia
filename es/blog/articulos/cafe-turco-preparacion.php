<h2>¿Qué Necesitas Para Preparar Café Turco?</h2>
<ul>
<li>En primer lugar deberás tener un cevze o cafetera turca, su base debe ser ancha y su boca angosta.</li>
<li>El café que usarás es de referencia arábica molido extra fino.</li>
<li>Azúcar opcional.</li>
<li>Tazas pequeñas. (En tuco se llaman Fincan)</li>
<li>Dulces turcos. (para presentacion)</li>
</ul>
<h3>Sigue los siguientes pasos</h3>
<ol>
<li>En el cevze, vas a calcular las tazas de café que quieres preparar, por cada taza de agua agrega dos cucharaditas de café.</li>
<li>Nota: si deseas poner azúcar es en este momento, pero el café tradicional por lo general no lleva dulce.</li>
<li>Pon al fuego y espera unos minutos hasta que el café genere algunas burbujas. En este momento, deberás tomar algunas burbujas y ponerlas en cada taza.</li>
<li>Vuelve a poner al fuego unos 3 minutos.</li>
<li>Alista las tazas, un poco de agua y dulces turcos.</li>
<li>Sirve con mucho cuidado para mantener las burbujas, pues un buen café debe tener la sensación de espesura en su superficie y gracias a su espuma.</li>
</ol>
