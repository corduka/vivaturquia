                        <h2>Top de los Museos más importantes de Estambul</h2>
<p>Existen muchos tipos de museos, los hay históricos, de ciencia, arqueológicos, de arte por mencionar algunos. Su propósito en sí, consiste en mostrar objetos de valor histórico, científico, artístico o cultural, permitiendo así, el crecimiento del capital cultural a las sociedades.</p>
<p>Los museos en Estambul son lo suficientemente diversos, queremos darte un breve recorrido por algunos de los museos más relevantes de la ciudad.</p>
<h3>Museo del Palacio de Topkapi</h3>
<p>Es uno de los mas famosos palacios en Estambul, se uso como centro administrativo durante el Imperio Otomano desde el siglo XV hasta el siglo XIX. También fue la morada de los sultanes del mismo imperio, así que su arquitectura es otomana, el palacio es espléndido por donde se le mire, al caminar por cada uno de sus jardines, salones y pasillos podrás evidenciar el gran poder y riqueza que tenían los sultanes del Imperio. Los espacios más visitado del palacio topkapi son el Harén y la habitación donde reposan algunos de los objetos mas valiosos del mundo: un diamante de 88 quilates, el cual perteneció a la madre de Napoleón y el puñal topkapi, el arma más cara del mundo.</p>
<ul>
    <li>Horario: De miércoles a lunes: de 9:00 a.m.  a 6:00 p.m. (martes no abre)</li>
    <li>Entradas: Palacio: 320 TL - 15 USD</li>
    <li>Harén: 150 TL - 7 USD</li>
    <li>Palacio + Harén: 420 TL - 19 USD</li>
    <li>Dirección: Eminönü, 34122 Cankurtaran</li>
</ul>
<h3>Museo de la Cisterna Basílica</h3>
<p>Bajo la ciudad de Estambul hay 60 cisternas de agua, que se construyeron para abastecer a la ciudad bizantina en caso de que llegase a ser atacada. Entre ellas la cisterna basílica construida en el año 532 d.c. es la más grande de todas las basílicas con 336 columnas de 9 metros de altura, 140 metros de largo y de 70 de ancho. Actualmente es un museo con bastante popularidad por su antigüedad y las dos cabezas de medusa que sostienen dos de sus columnas, tanto así que en el año 2014 fue reconocido con el premio “Travellers’ Choice 2014” por millones de viajeros que realizan sus votaciones por TripAdvisor.</p>
<ul>
    <li>Horario: Todos los días de 9:00 a.m. a 6:30 p.m.</li>
    <li>Entradas: 136 TL - 6 USD</li>
    <li>Dirección: Alemdar, Yerebatan Cd. 1/3, 34110 Fatih/İstanbul</li>
</ul>
<h3>Museo de Arqueología de Estambul</h3>
<p>Está ubicado en uno de los jardines del palacio de Topkapi y se inauguró en el año 1891, posee alrededor de 20 salas con diversas colecciones; arqueología clásica, bizantinas, exposición de Estambul a lo largo de su historia, diferentes sarcófagos entre ellos el sacófrago de Alejandro Magno. Hoy, este museo figura como uno de los más grandes del mundo.</p>
<ul>
    <li>Horario: Todos los días 9:00 a.m. a 8:00 p.m.</li>
    <li>Entradas: 200 TL - 10 USD</li>
    <li>Dirección: Cankurtaran, 34122 Fatih/Provincia de Estambul</li>
</ul>
<h3>Museo del Palacio de Dolmabahce</h3>
<p>Tiene un estilo neobarroco y su importancia radica en que fue lugar administrativo durante el periodo Otomano y residencia de los últimos seis sultanes y de Mustafa Kemal Atatürk, fundador de la República de Turquía. Se encuentra a orillas del Bósforo en el distrito de Beşıktas  construido en el siglo XIX, 45.000 metros cuadrados, 285 habitaciones, 68 baños y una cantidad colosal de alfombras son algunas de las cosas que compone el lugar.</p>
<ul>
    <li>Horario: De martes a domingo de 9:00 a.m.a 4:00 p.m.</li>
    <li>Entradas: 60 TL - 3 USD / Harén 20 TL - 1 USD</li>
    <li>Dirección: Vişnezade, Dolmabahçe Cd., 34357 Beşiktaş/İstanbul</li>
</ul>
<h3>Museo de la inocencia</h3>
<p>Ubicado en Çukurcuma el Museo literario fue inaugurado por el autor y Nobel turco Orhan Pamuk en el año 2012, la finalidad de este museo es darle vida a su libro “Museo de la inocencia” una historia de amor en la que se van implicando objetos que van a tener relevancia durante toda la historia, los mismos que el autor fue coleccionando personalmente y que suman más de mil objetos en una casa del siglo XIX.</p>
<p>El museo fue ganador en el año 2014 del premio al Mejor Museo Europeo.</p>
<ul>
    <li>Horario: De lunes a domingo de 10:00 a.m. A 6:00 p.m.</li>
    <li>Entradas:  150 TL - 7 USD</li>
    <li>Estudiantes: 75 TL - 3 USD</li>
    <li>Audio guía 10 TL</li>
    <li>Dirección: Sinanpaşa, Beşiktaş Cd. No:6 D:1, 34353 Beşiktaş/İstanbul</li>
</ul>
<p>Para comprar tus entradas y ver mas información ingresa al sitio oficial haciendo clic en el siguiente link info@masumiyetmuzesi.org.</p>
<h3>Museo de artes turcas e islámicas</h3>
<p>Este es uno de los museos mas importantes de Estambul, está ubicado en el distrito Eminönü ocupando el que fue el palacio de Ibrahim Paşa  posee una amplia colección de esculturas, alfombras, cerámicas, antiguas versiones del Corán, ejemplos de caligrafía islámica y una sección etnográfica, maravillosa exposición que permite entrever algunos grupos étnicos específicamente nómadas turcos.</p>
<ul>
    <li>Horario: De martes a domingo de 9:00 a.m. a 6:00 p.m.</li>
    <li>Entradas: 50 TL, menores de 12 años ingresan de manera gratuita.</li>
    <li>Dirección: At Meydani 46, Sultanahmet.</li>
</ul>
<h3>Museo de arte Moderno Estambul</h3>
<p>Este museo se ubica en el distrito Beyoğlu sobre el mar Mármara, fue fundado en el año 2004, tiene  exposiciones permanentes de arte contemporáneo principalmente de artistas turcos de los siglos XIX y XX en el segundo piso y en la planta baja se realizan exposiciones temporales de otros artistas.</p>
<ul>
    <li>Horario: De martes a domingos de 10:00 a 18:00. Jueves, de 10:00 a 20:00</li>
    <li>Entradas: 15 TL - 12 TL  grupos de mas de 10 personas.</li>
    <li>Dirección:  Bilişim . Kilic Ali Pasa District, Tophane Iskele Street, No:1/1 34433 Beyoglu-Istanbul</li>
</ul>
<h3>Museo del juguete de Estambul en Goztepe Kadikoy</h3>
<p>Está ubicado en el barrio Göztepe en el distrito de Kadiköy, se fundó en el año 2005 en abril 23, fecha que celebra el día del niño y la Soberanía Nacional, el museo alberga más de 4.000 juguetes que datan desde el siglo XVIII hasta la actualidad; la intención del museo es mostrar la historia de una manera diferente, los adultos se transportarán a sus infancias pues los juguetes van desde lo tradicional, lo doméstico o juguetes famosos a nivel mundial representando la historia de varios países.</p>
<p>Su fundador es un reconocido poeta turco, quien al adquirir varios juguetes en subastas decidió crear el museo inspirado en el museo de Nuremberg.</p>
<ul>
    <li>Horario: Todos los días de 11:00 a.m. a 6:00 a.m.</li>
    <li>Entradas: 60 TL - 3 USD</li>
    <li>Dirección: Göztepe, Dr. Zeki Zeren Sk No:17, 34730 Kadıköy/İstanbul</li>
</ul>
<h3>Museo de Turquía en miniatura (Miniaturk)</h3>
<p>Está ubicado a orillas del cuerno de oro, existe desde el 2003 y es uno de los parques miniatura más grandes del mundo. El museo al aire libre te lleva a conocer as obras arquitectónicas mas importantes de Turquía a una escala minimalista y por demás hermosa. Incluye 62 piezas de Estambul, 60 piezas de la región de Anatolia entre muchas otras curiosidades. ¡Agéndate!</p>
<ul>
    <li>Horario: Todos los días de 9:00 a.m. a 12:00 a.m.</li>
    <li>Entradas: 150 TL - 6 USD. Gratis para profesores, estudiantes, policías y militares.</li>
    <li>Dirección: Örnektepe, İmrahor Cd. No:7, 34445 Beyoğlu/İstanbul</li>
</ul>
<h3>Museo de Mosaicos</h3>
<p>El museo está ubicado en el distrito de Sultanahmet, se construyó en el mismo lugar donde el emperador romano Constantino tenía su palacio entre los siglos IX - XII, pese a su destrucción y posteriores excavaciones fueron encontradas diversas piezas de mosaicos que hacían parte del palacio.</p>
<p>De esta manera lo que expone el museo no son mas que los mosaicos bizantinos, que dan cuenta de la vida diaria, algunas actividades de caza y batallas con animales mitológicos.</p>
<ul>
    <li>Horario: Todos los días de 9:00 a.m. a 6:30 p.m.</li>
    <li>Entradas: 120 TL- 6 USD</li>
    <li>Dirección: Sultanahmet Mahallaesi Kabasakal Cad. Arasta Çarşısı Sok. No:53 Fatih</li>
</ul>
<h3>Museo Naval Nacional</h3>
<p>Está Ubicado en el distrito de Besiktas, frente a la costa del Bósforo, tiene una apreciable arquitectura contemporánea, 20.000 metros cuadrados y más de 20.000 objetos, haciendo honra al pasado del Imperio Otomano y su relación con el mar donde algunos importantes episodios como la conquista de Constantinopla en 1453 se dieron allí; en las aguas del Bósforo.</p>
<ul>
    <li>Horario: martes a viernes de  9:00 a.m. hasta las 5:00p.m. Los fines de semana de 10 a.m. a 6:00 p.m.</li>
    <li>Entradas: 170 TL - 8 USD</li>
    <li>Niños de 0-4 años ingresan gratis</li>
    <li>5-18 años: 105 TL - 5 USD</li>
    <li>Paquetes familiares 480 TL - 24 USD</li>
    <li>Dirección: Sinanpaşa, Beşiktaş Cd. No:6 D:1, 34353 Beşiktaş/İstanbul</li>
</ul>
