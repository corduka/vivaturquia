<h2>¿Sabes cuál es la capital de Turquía?</h2>
<p>Con frecuencia tendemos a creer que Estambul es la capital de Turquía lo cual es falso, si bien, fue capital del Imperio Romano de Oriente y del Imperio Otomano, esto cambió en el año 1923, con la fundación de la República de Turquía motivo por el cual, se traslada la capital a la ciudad de Ankara.</p>
<h2>¿Sabes cuál es la moneda de Turquía?</h2>
<p>La moneda de Turquía es la Lira Turca, encontrarás 6 (seis) billetes: 5, 10, 20, 50, 100 y 200 liras.  1 USD equivale a 18 TL.</p>
<p>Con este dato podrás hacer las conversiones más básicas al momento de planear tu viaje.</p>
<h2>¿Turquía es un país musulmán o árabe?</h2>
<p>Pese a la fuerte influencia de la religión musulmana no se puede considerar a 
Turquía como tal, dado que desde 1923 gracias a su fundador Mustafa Kemal Atatürk, se declaró este como un Estado laico.</p>
<h2>¿Sabes cómo son los baños en Turquía?</h2>
<p>Parece una pregunta simple pero no lo es, a diferencia del retrete clásico o del más conocido en latinoamérica, existen dos tipos de retrete:</p>
<p>Tipo letrina, es un tipo de inodoro aplanado a la altura del piso, muy antiguo y lo verás muy poco en casas modernas, sin embargo tendrás la oportunidad de verlos en el Aeropuerto de Estambul.</p>
<p>El segundo tipo de inodoro que vas a encontrar es el que casi todos conocemos, con una pequeña diferencia y es que este tiene una pequeña manguera en la parte trasera, ya sabes por aquello de la limpieza. ¿Conoces alguno?</p>
<h2>¿Sabes algo de turco?</h2>
<p>Una de las cosas más maravillosas de viajar es aprender tantas cosas de la cultura como podamos, aquellas cosas inmateriales que se convertirán en parte de nosotros. Aquí podrás encontrar las expresiones más básicas y útiles para que puedas pasar de ser el típico turista aburrido y te permitas interactuar de una manera más cálida con los locales.</p>
<p>El saludo es importante ¿no? para saludar con un hola; vas a decir:</p>
