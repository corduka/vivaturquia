<h2>Puerta de Adriano</h2>
<p>La emblemática puerta que divide el sector de Kaleiçi -ciudad antigua y moderna-, se construyó en honor al emperador Adriano; es un arco del triunfo compuesto por tres arcos y columnas de mármol blanco, los detalles de tan esbelta pieza están intactos, así como las torres que colindan a cada uno de sus lados. La puerta de Adriano es considerada una de las mas bellas de la región de la antiguamente llamada Asia Menor (lo que hoy abarca Antalya y los Montes Tauro).</p>
<h2>Museo Arqueológico de Antalya</h2>
<p>Fundado en 1922 con el fin de proteger los hallazgos arqueológicos que se fueron haciendo en la zona turca, las muestras de este Museo son bien particulares, debido a que algunas datan de los periodos comprendidos entre el paleolítico y la época romana. Su organización es de resaltar, hay espacios para todos incluidos los niños, quienes podrán ver una selección de juguetes de la antigüedad.</p>
<h2>Acuario de Antalya</h2>
<p>De los acuarios mas auténticos que podrás visitar, los protagonistas son los tiburones de aguas mediterráneas que junto con buzos profesionales recrean el lugar mientras estos últimos dan de comer a las increíbles depredadores. El acuario posee 40 tanques temáticos y un túnel acuario de 130 metros de ancho que permite admirar no solo la diversidad de especies marinas y los cerca de 40 mil animales para los que fue construido el recinto sino restos de naufragios y sus barcos antiguos estatuas decorativas y hasta la réplica de un avión de combate de la II Guerra Mundial.</p>
<h2>Cascada Düden</h2>
<p>A tan solo 12 km del nordeste de Antalya encontrarás un espectáculo deslumbrante, en las aguas de las cascadas Düden, formadas por la desembocadura del río Düden en el Mediterráneo, ahí está el encanto del lugar, la fusión del agua de río con el azul profundo del mediterráneo logran hacer del lugar un cuadro digno de admirar.</p>
