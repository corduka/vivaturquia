</li>
<li><h2>Xantos</h2>
<p>Xanthos es una ciudad antigua ubicada en la provincia de Antalya, era la capital y el centro Administrativo más grande de Licia. Importantes vestigios históricos siguen siendo visitados hoy en día: los templos dedicados a Apolo, Artemisa y Leto, además de los templos hay una iglesia del período cristiano temprano y la famosa tumba de las Arpías. </p>
<p>En conclusión, a lo largo del territorio turco hay historia viva, cada ciudad antigua es valiosa para entender algunas dinámicas que fueron configurando la vida económica, política y social que se ha extendido hasta nuestros días, y ni qué decir de la arquitectura y el arte. Muchos aseguran que es la cuna de muchas civilizaciones, no lo sabemos, pero de lo que sí estamos seguros es que hay mucho por ver y por aprender ¿A cuál de estas ciudades te gustaría ir? Recuerda que Viva Turquía te lleva a cualquiera de ellas.</p>
