<li><h2>Troya</h2>
<p>Ubicada en la provincia de Çanakkale, Troya es otra ciudad antigua que está llena de historia y mitología. Fue el sitio de la Guerra de Troya, como se relata en el poema épico de Homero, la Ilíada. Los visitantes pueden explorar las ruinas de la ciudad, incluido el famoso Caballo de Troya.</p>
