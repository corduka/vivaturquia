<li><h2>Hierápolis</h2>
<p>Hierápolis es una ciudad antigua ubicada en la provincia de Denizli, Pamukkale. Fue una ciudad balnearia en la antigüedad que alberga numerosas ruinas bien conservadas, incluida la Necrópolis, el teatro y una colección de preciosas tumbas romanas. </p>
<p>Es un privilegio visitar esta ciudad porque encontrarás al finalizar el recorrido la inigualable montaña blanca también conocida como “Castillo de Algodón”.</p>
