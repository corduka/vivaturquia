<li><h2>Perge</h2>
<p>Perge es una ciudad antigua ubicada en la provincia de  Antalya. Es considerada una de las ciudades más ricas y bellas del mundo antiguo, y cómo no creerlo si posee una privilegiada ubicación: La región de Panfila, en su momento Perge fue la capital de Panfilia debido a que era una de las regiones mejor organizadas de toda la región. Actualmente el lugar ofrece a sus visitantes el Museo al aire libre, donde está ubicado el gran teatro que podía reunir a más de quince mil personas. </p>
</li>
<li><h2>Stratonikeia</h2>
<p>Stratonikeia es también conocida como la ciudad de los gladiadores, una ciudad antigua ubicada en la provincia de  Antalya, más específicamente en la ciudad de Mugla. La ciudad alberga ruinas de aproximadamente 3000 años de antigüedad, y conserva diferentes tipos de ruinas y edificios de diferentes épocas. También es conocida por ser una de las ciudades de mármol más grandes del mundo. </p>
</li>
<li><h2>Aspendos</h2>
<p>Aspendos es una ciudad antigua ubicada en la provincia de Antalya, en el distrito de Serik. Como muchas de las ciudades antiguas que se ubican en el mar Mediterráneo, fue una ciudad muy independiente, y al imprimir su propio dinero y tener control de su economía fue una de las más importantes y ricas.</p>
<p>La imposición económica les permitió construir un desarrollado acueducto que hoy en día los ingenieros lo consideran una obra maestra hidráulica. Otra importante huella de la ciudad antigua de Aspendos es su Teatro, es quizá uno de los teatros más bellos pues se mantiene en pie a pesar del paso de los años.</p>
</li>
<li><h2>Mileto</h2>
<p>Mileto es una ciudad antigua ubicada en la provincia de Aydın. Fue una de las ciudades más importantes de la antigua Grecia, debido a que contaba con la desembocadura de un río y cuatro puertos. Algunos de los lugares que deberás visitar en esta gran ciudad son: Teatro de Mileto, Baños de Faustina, La Puerta del Puerto y el Delfinion, la puerta principal de Mileto. </p>
</li>
<li><h2>Pérgamo</h2>
<p>Pérgamo es una ciudad legendaria y antigua ubicada en la provincia de Esmirna. Fue incluida en la lista del Patrimonio de la Humanidad en el año 2014. En el siglo IV a.c fue una de las ciudades más importantes de la región. Podrás ver un teatro con capacidad de diez mil personas, el Asclepion un lugar consagrado al dios de la medicina y el Museo Arqueológico de Pérgamo.</p>
</li>
<li><h2>Myra</h2>
<p>Myra es una ciudad antigua de Licia ubicada en la provincia de Antalya. Es conocida como la Pompeya de Anatolia, dicha semejanza se debe a que la ciudad se fue cubriendo por la actividad del río y sus sedimentos. A pesar de que es poco famosa hoy en día, la ciudad tiene mucho por ofrecer a sus visitantes, por ejemplo: El templo de Artemisa, el Teatro de La Ciudad Antigua,  su arquitectura con relieves donde destacan figuras como sirenas, águilas, míticas criaturas marinas y medusa. También hay que destacar la peculiar necrópolis que parecen casas o pequeños templos, esto se debe a que en la antigua Licia la vida no tenía fin después de la muerte, así que sus tumbas, además de parecer un hogar tenían algunos de sus objetos personales e incluso ropa. </p>
